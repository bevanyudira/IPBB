function SidebarMenuButton({
  asChild = false,
  isActive = false,
  variant = "default",
  size = "default",
  tooltip,
  className,
  ...props
}: React.ComponentProps<"button"> & {
  asChild?: boolean;
  isActive?: boolean;
  tooltip?: string | React.ComponentProps<typeof TooltipContent>;
} & VariantProps<typeof sidebarMenuButtonVariants>) {
  const Comp = asChild ? Slot : "button";
  const { isMobile, state } = useSidebar();
  const pathname = usePathname();

  // compute active state: prefer explicit isActive prop, otherwise infer from child href or props.href
  let computedActive = !!isActive;
  if (!computedActive) {
    const child = (props as any).children as React.ReactNode;
    if (asChild && React.isValidElement(child)) {
      const href = (child.props && (child.props.href || child.props.to)) as string | undefined;
      if (href && pathname) {
        const base = href.endsWith("/") ? href.slice(0, -1) : href;
        const current = pathname.endsWith("/") ? pathname.slice(0, -1) : pathname;
        if (current === base || current.startsWith(base + "/")) computedActive = true;
      }
    }
    const maybeHref = (props as any).href as string | undefined;
    if (!computedActive && maybeHref && pathname) {
      const base = maybeHref.endsWith("/") ? maybeHref.slice(0, -1) : maybeHref;
      const current = pathname.endsWith("/") ? pathname.slice(0, -1) : pathname;
      if (current === base || current.startsWith(base + "/")) computedActive = true;
    }
  }

  const button = (
    <Comp
      data-size={size}
      data-active={computedActive ? "true" : "false"}
      className={cn(sidebarMenuButtonVariants({ variant, size }), className)}
      {...props}
    />
  );

  if (!tooltip) {
    return button;
  }

  if (typeof tooltip === "string") {
    tooltip = {
      children: tooltip,
    };
  }

  return (
    <Tooltip>
      <TooltipTrigger asChild>{button}</TooltipTrigger>
      <TooltipContent
        side="right"
        align="center"
        hidden={state !== "collapsed" || isMobile}
        {...tooltip}
      />
    </Tooltip>
  );
}

